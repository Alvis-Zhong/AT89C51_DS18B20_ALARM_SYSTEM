/****************************************************************************************
	*@brief		蜂鸣器与LED的驱动函数
	*@param		t用于设置蜂鸣器的频率
	*@retvel	无
*****************************************************************************************/
void Alarm(uchar t)
{
 	uchar i,j;
	for(i=0;i<200;i++)
	{
	 	BEEP = ~BEEP;
		for(j=0;j<t;j++);
		LED = ~LED ;
	}
}
